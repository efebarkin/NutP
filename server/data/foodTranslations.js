// Çeviri yardımcı fonksiyonları
export const translateFoodName = (englishName) => {
  // İngilizce ismi küçük harfe çevir
  const lowerName = englishName.toLowerCase();
  
  // Her kelimeyi ayrı ayrı çevirmeyi dene
  const words = lowerName.split(' ');
  const translatedWords = words.map(word => foodTranslations[word] || word);
  
  // Çevrilen kelimeleri birleştir
  return translatedWords.join(' ');
};

export const translateWithFallback = (englishName) => {
  const translated = translateFoodName(englishName);
  // Eğer hiçbir kelime çevrilmediyse orijinal ismi döndür
  return translated === englishName.toLowerCase() ? englishName : translated;
};
